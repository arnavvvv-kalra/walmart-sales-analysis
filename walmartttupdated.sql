/*PROJECT Q1 FIND total quantity and no of transactions/payment method */
select distinct payment_method,count(*) as no_transaction,
SUM(quantity) as total_quantity from walmart
group by payment_method

/*PROJECT Q2 Identify the highest rated category in each branch,displaying branch and category avg rating*/ 
select branch,category,avg_rating FROM (
select branch,category,avg(rating) as avg_rating,RANK()OVER(PARTITION BY branch ORDER BY avg(rating) DESC)
as rnk from walmart group by branch,category)as new_table where rnk=1
/*PROJECT Q3: FIND THE BUSIEST DAY FOR EACH BRANCH BASED ON NO OF INVOICES*/
